# Join List
# Given a list of words, 
# write a program that joins them into a single string 
# with a space as the separator and prints the result.
# Given lists of words
list1 = ["Alice", "kittu", "Harsita", "Anupam"]
list2 = ["Gulzar", "zofishan", "Fatima"]

# Combine both lists into one
combined_list = list1 + list2

# Join the combined list into a single string with a space as the separator
single_string = " ".join(combined_list)

# Print the result
print(single_string)




